

fun main(args:Array<String>){

    /*Put "var" when Value is changing for that variables |
    var = Read + Write */

    /*Put "val" when Value is constant(fix) |
    val = Read only*/

    var name:String="Harvey"
    var age:Int=28
    var dep:String?
    dep="Computer Science"
    val T:Float=3.14f

    println("Name: "+ name)
    println("Age: "+ age)
    println("Department: "+ dep)
    println("Pi: "+ T)


}
